export const getDataset = (question: any) => {
  const { aAnswerCount, bAnswerCount, cAnswerCount, dAnswerCount, correctAnswer } = question;

  return [
    {
      data: [
        `${(aAnswerCount / 10) * 100}`,
        `${(bAnswerCount / 10) * 100}`,
        `${(cAnswerCount / 10) * 100}`,
        `${(dAnswerCount / 10) * 100}`,
      ],
      borderColor: '#9852f9',
      backgroundColor: [
        `${correctAnswer === 'a' ? '#0fb56d' : '#f55d7a'}`,
        `${correctAnswer === 'b' ? '#0fb56d' : '#f55d7a'}`,
        `${correctAnswer === 'c' ? '#0fb56d' : '#f55d7a'}`,
        `${correctAnswer === 'd' ? '#0fb56d' : '#f55d7a'}`,
      ],
    },
  ];
};

